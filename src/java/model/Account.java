package model;

/**
 *
 * @author Account
 */
public class Account {
    private String name, userName, passWord, email;
    private int role;

    public Account() {
    }

    public Account(String name, String userName, String passWord, String email, int role) {
        this.name = name;
        this.userName = userName;
        this.passWord = passWord;
        this.email = email;
        this.role = role;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getUserName() {
        return userName;
    }

    public void setUserName(String userName) {
        this.userName = userName;
    }

    public String getPassWord() {
        return passWord;
    }

    public void setPassWord(String passWord) {
        this.passWord = passWord;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public int getRole() {
        return role;
    }

    public void setRole(int role) {
        this.role = role;
    }

    @Override
    public String toString() {
        return "Account{" + "name=" + name + ", userName=" + userName + ", passWord=" + passWord + ", email=" + email + ", role=" + role + '}';
    }
    
    
}
